package com.bezkoder.spring.login.models;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.*;

@Entity
@Table(name = "product")
public class user1 {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
     int pro_id;
	@Column(name = "pro_name")
	 String pro_name;

	@Column(name = "short_line")
	 String short_line;

	@Column(name = "detail_des")
	 String detail_des;

	public user1(int pro_id, String pro_name, String short_line, String detail_des) {
	}
	public user1(String pro_name, String short_line, String detail_des) {
		this.pro_name = pro_name;
		this.short_line = short_line;
		this.detail_des = detail_des;
	}
}